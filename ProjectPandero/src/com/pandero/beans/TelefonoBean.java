package com.pandero.beans;

public class TelefonoBean {
	private int idcorrelativo;
	private int idtelefono_socio;
	private String telefono;
	private String idSocioaux;
	
	public int getIdcorrelativo() {
		return idcorrelativo;
	}
	public void setIdcorrelativo(int idcorrelativo) {
		this.idcorrelativo = idcorrelativo;
	}
	public String getTelefono() {
		return telefono;
	}
	public void setTelefono(String telefono) {
		this.telefono = telefono;
	}
	public int getIdtelefono_socio() {
		return idtelefono_socio;
	}
	public void setIdtelefono_socio(int idtelefono_socio) {
		this.idtelefono_socio = idtelefono_socio;
	}
	public String getIdSocioaux() {
		return idSocioaux;
	}
	public void setIdSocioaux(String idSocioaux) {
		this.idSocioaux = idSocioaux;
	}
	
	
}
